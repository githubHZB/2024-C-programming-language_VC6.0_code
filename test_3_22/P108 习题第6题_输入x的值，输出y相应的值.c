// 引入标准输入输出头文件stdio.h
#include<stdio.h>

// 定义主函数main
int main()
{
    // 定义两个整型变量x和y
    int x, y;

    // 从用户处接收并读取一个整数，存储到变量x中
    scanf("%d", &x);

    // 判断条件：如果x小于未赋值的变量y（此处y没有初始化，比较结果未定义）
    if (x < y) 
    {
        // 如果条件成立，则将x的值赋给y
        y = x;
    }
    // 否则，进一步判断x是否在1至9之间（包含1而不包含10）
    else if (x >= 1 && x < 10) 
    {
        // 如果条件成立，则按照公式计算y的值：y = 2 * x - 1
        y = 2 * x - 1;
    }
    // 如果上述条件都不满足，则执行此部分代码
    else 
    {
        // 根据公式计算y的值：y = 3 * x - 11
        y = 3 * x - 11;
    }

    // 输出计算得到的y的值
    printf("y=%d\n", y);

    // 主函数返回0，表示程序执行成功
    return 0;
}

// 注：程序在执行前需要确保变量y有一个初始值，否则与x进行比较时结果未定义。



