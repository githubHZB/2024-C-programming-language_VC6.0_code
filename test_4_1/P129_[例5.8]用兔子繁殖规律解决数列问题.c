// 引入标准输入输出头文件stdio.h
#include<stdio.h>

// 定义主函数main
int main()
{
	// 初始化两个整数变量f1和f2，赋值分别为1
	int f1 = 1, f2 = 1;
	// 定义一个整数变量f3用于存储斐波那契数列的下一个数
	int f3;
	// 定义一个循环变量i，用于控制循环次数
	int i;

	// 打印初始的前两个斐波那契数列项（分别是1和1），格式化输出，每项占12个字符宽度
	printf("%12d\n%12d\n", f1, f2);

	// 使用for循环计算并打印斐波那契数列的下38项
	for (i = 1; i <= 38; i++)
	{
		// 计算当前项f3为前两项之和：f3 = f1 + f2
		f3 = f1 + f2;

		// 格式化输出当前项f3，每项占12个字符宽度
		printf("%12d\n", f3);

		// 更新前两项的值，将f2的值赋给f1，将当前f3的值赋给f2，准备计算下一项
		f1 = f2;
		f2 = f3;
	}

	// 主函数返回0，表示程序正常结束
	return 0;
}